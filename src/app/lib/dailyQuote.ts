export function getLocalDateKey(date = new Date()) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function getDayNumber(dateKey: string) {
  const [year, month, day] = dateKey.split('-').map(Number);
  if (!year || !month || !day) return 0;
  return Math.floor(Date.UTC(year, month - 1, day) / 86_400_000);
}

export function getDailyQuote(date = new Date(), quotes = DAILY_QUOTES): DailyQuote {
  if (quotes.length === 0) {
    return { text: 'Хороший день начинается с ясного плана.' };
  }

  const dateKey = getLocalDateKey(date);
  const index = getDayNumber(dateKey) % quotes.length;
  return quotes[index];
}
